var s = $(".sticker");
var pos = s.position();
$(window).on('scroll', function () {
    var windowpos = $(window).scrollTop();
    if (windowpos > pos.top) {
        s.addClass("stick");
    } else {
        s.removeClass("stick");
    }
});
